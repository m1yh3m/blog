const bucketSort = require('./BucketSort.node')

const randomN = n => Array(n).fill(0).map(i => Math.floor(Math.random() * n))

function test1() {
    const input = randomN(100)
    const bucketSize = 10
    const out = bucketSort(input, bucketSize)
    console.log(input, out, out.reduce((a, c) => ([...a, ...c]), []))
}

module.exports = {
    test1
}

